//Для целого числа N (где N > 0), 
//напиши функцию `factorialProduct()`, используя цикл **for**. Функция должна вернуть произведение N! = 1 * 2 * ... * N в виде вещественного числа.
function factorialProduct(N) {
    let sum = 1;
    for (let i = 1; N >= i; i++) {
        sum = sum * i;
    }
    return sum;
}

