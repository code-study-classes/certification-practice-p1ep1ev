//### Задача 1
//Для двух целых чисел A и B (где A < B), 
//напиши функцию `sumOfSquares()`, используя цикл **for**. Функция должна вернуть сумму квадратов всех целых чисел от A до B включительно.
function sumOfSquares(A, B) {
    let sum = 0;
    for (let i = A; i <= B; i += 1) {
      sum += i ** 2;
    }
    return sum;
}

